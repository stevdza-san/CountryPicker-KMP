fun Country.formatedName(): String {
    return name.replace(Regex("([a-z])([A-Z])"), "$1 $2")
}

fun List<Country>.filterByQuery(query: String): List<Country> {
    val queryLower = query.lowercase()
    return this.filter {
        it.currency.lowercase().contains(queryLower) ||
                it.formatedName().lowercase().contains(queryLower) ||
                it.dialCode.toString().lowercase().contains(queryLower) ||
                it.isoCode.lowercase().contains(queryLower)
    }
}